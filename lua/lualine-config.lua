local array_art_interval = 0.02

vim.g.lualineanim = 0
vim.keymap.set('n', '<A-x>a', function() vim.g.lualineanim = (vim.g.lualineanim + 1) % 3 end)
function get_move()
    local t = math.floor(math.fmod(os.clock(), (table.getn(array_art)) * array_art_interval) / array_art_interval);
    return array_art[t+1]
end

function get_lualine()
    if vim.g.lualineanim == 0 then
        return ''
    elseif vim.g.lualineanim == 1 then
        return get_move()
    elseif vim.g.lualineanim == 2 then
        return os.date("%x %H:%M:%S")
    end
end

